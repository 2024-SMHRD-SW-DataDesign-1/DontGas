package com.dontgas.dontgas.entity;

public class WeatherData {
    private float temp;
    private float hum;
    private float wind_deg;
    private float wind_sp;

    // Getters and Setters
    public float getTemp() { return temp; }
    public void setTemp(float temp) { this.temp = temp; }

    public float getHum() { return hum; }
    public void setHum(float hum) { this.hum = hum; }

    public float getWind_deg() { return wind_deg; }
    public void setWind_deg(float wind_deg) { this.wind_deg = wind_deg; }

    public float getWind_sp() { return wind_sp; }
    public void setWind_sp(float wind_sp) { this.wind_sp = wind_sp; }
}

